package com.deisi.inqueritos.apiclient;


public class Teacher {
    private String name;
    private String idInqueritos;
    private String idLusofona;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdInqueritos() {
        return idInqueritos;
    }

    public void setIdInqueritos(String idInqueritos) {
        this.idInqueritos = idInqueritos;
    }

    public String getIdLusofona() {
        return idLusofona;
    }

    public void setIdLusofona(String idLusofona) {
        this.idLusofona = idLusofona;
    }
}
